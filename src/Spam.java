public class Spam {

    private String word;
    private float ratio;

    //Constructors:

    public Spam(String word, float ratio) {
        this.word=word;
        this.ratio=ratio;
    }

    //Getters:

    public float getRatio() {
        return ratio;
    }

    public String getWord() {
        return word;
    }

    //Setters:

    public void setRatio(float ratio) {
        this.ratio = ratio;
    }

    public void setWord(String word) {
        this.word = word;
    }

    //Methods:



}
